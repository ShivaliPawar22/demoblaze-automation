
public class SwapNumbers {

	public static void main(String[] args) {
		
		int a = 5;
		int b = 10;
		int c= 15;

	
	    a = a+b;      //a = a+b;   Swapping of two numbers	      
		b = a-b;      //b = a-b;
		c = c-a;      //a = a-b;
		
		
		
		
		System.out.println("Swapped numbers are a is " + a + " and b is " + b + " and c is " + c);
}
}